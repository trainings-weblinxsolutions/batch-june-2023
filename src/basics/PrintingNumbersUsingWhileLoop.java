package basics;
public class PrintingNumbersUsingWhileLoop {
   public static void main(String []args) {
   
   int i=1;
   	System.out.println("Starting to print numbers in ascending order :");

   while (i <=5 ){
   System.out.println(i);
   i++;
   }
   	System.out.println("All numbers printed");



 int j=5;
   	System.out.println("Starting to print numbers in descending order :");

   while (j >=1 ){
   System.out.println(j);
   j--;
   }
   	System.out.println("All numbers printed");

   
   }
   }