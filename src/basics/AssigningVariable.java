package basics;
class AssigningVariable {
  public static void main(String args[]) {
  
  
  int a = 10;
  int b = a;
  
  System.out.println("Value of a is : "+a);
  System.out.println("Value of b is : "+b); 
  
  b = 200; 
  
  System.out.println("Value of a is : "+a);
  System.out.println("Value of b is : "+b); 
  
  
  }
  }
